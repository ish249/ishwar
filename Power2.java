package algorithms;
import java.util.*;
public class Power2 {
	
	    public static void main(String[] args)
	    {
	        Scanner s=new Scanner(System.in);
	        int N=s.nextInt();
	        int i=0;
	        while(i<=N)
	        {   
	            System.out.println(Math.pow(2,i));
	            i++;
	        }

	        
	    
	}
}
